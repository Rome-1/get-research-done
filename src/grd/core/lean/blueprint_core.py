def _sanitize_id(plan_id: str) -> str:
    """Convert a plan ID like 'analysis-A' to a valid LaTeX label."""
    return re.sub(r"[^a-zA-Z0-9_]", "_", plan_id)


def _lean_module_name(plan_id: str) -> str:
    """Convert a plan ID to a Lean module name: 'analysis-A' -> 'AnalysisA'."""
    parts = re.split(r"[-_ ]+", plan_id)
    return "".join(p.capitalize() for p in parts if p)


def _generate_content_tex(
    nodes: list[BlueprintNode],
    edges: list[tuple[str, str]],
    phase_name: str | None,
    phase_number: str,
    project_name: str | None,
) -> str:
    """Generate the leanblueprint-compatible content.tex."""
    title = phase_name or f"Phase {phase_number}"
    header = textwrap.dedent(f"""\
        % Blueprint for {title}
        % Generated by grd lean init-blueprint
        %
        % Leanblueprint-compatible LaTeX.  Each node maps to a GRD plan entry;
        % \\uses edges mirror the plan dependency graph.  Fill in informal
        % statements, then add \\lean{{Module.Name}} bindings and \\leanok once
        % the Lean proof type-checks.

        \\title{{{title}}}
        \\author{{Generated by Get Research Done}}

        \\begin{{document}}
        \\maketitle
    """)

    body_parts: list[str] = []
    for node in nodes:
        label = _sanitize_id(node.id)
        lean_ref = f"  \\lean{{Proofs.{_lean_module_name(node.id)}}}\n" if node.lean_name else ""
        uses = ""
        deps = [dep for (src, dep) in edges if src == node.id]
        if deps:
            uses = f"  \\uses{{{', '.join(_sanitize_id(d) for d in deps)}}}\n"
        objective_comment = ""
        if node.objective:
            wrapped = textwrap.fill(node.objective, width=76, initial_indent="% ", subsequent_indent="% ")
            objective_comment = f"{wrapped}\n"

        body_parts.append(
            f"{objective_comment}"
            f"\\begin{{{node.kind}}}\\label{{{label}}}\n"
            f"{uses}"
            f"{lean_ref}"
            f"  % TODO: state the informal claim here\n"
            f"\\end{{{node.kind}}}\n"
        )

    footer = "\\end{document}\n"
    return header + "\n".join(body_parts) + "\n" + footer


def _generate_lean_root(nodes: list[BlueprintNode]) -> str:
    """Generate Blueprint.lean that imports all proof modules."""
    lines = [
        "-- Blueprint module root",
        "-- Generated by grd lean init-blueprint",
        "",
    ]
    for node in nodes:
        mod = _lean_module_name(node.id)
        lines.append(f"import Proofs.{mod}")
    lines.append("")
    return "\n".join(lines)


def _generate_lean_stub(plan_id: str) -> str:
    """Generate a stub Lean proof file for one plan entry."""
    mod = _lean_module_name(plan_id)
    return textwrap.dedent(f"""\
        /-
        Proof stub for plan: {plan_id}
        Generated by grd lean init-blueprint — fill in the formal statement
        and proof below, then run `grd lean typecheck-file` to verify.
        -/

        -- TODO: add imports (e.g. import Mathlib.Tactic)

        /-- Formal statement for {plan_id}. Replace `sorry` with a proof. -/
        theorem {mod} : True := by
          sorry
    """)


def _generate_lakefile(phase_name: str | None, phase_number: str) -> str:
    """Generate a minimal lakefile.lean."""
    pkg = f"Blueprint{phase_number.replace('.', '_')}"
    return textwrap.dedent(f"""\
        import Lake
        open Lake DSL

        package «{pkg}» where
          leanOptions := #[
            ⟨`autoImplicit, false⟩
          ]

        @[default_target]
        lean_lib «Proofs» where
          srcDir := "."
    """)


def _generate_lean_toolchain(lean_version: str | None) -> str:
    """Generate lean-toolchain file."""
    version = lean_version or "leanprover/lean4:v4.14.0"
    if not version.startswith("leanprover"):
        version = f"leanprover/lean4:{version}"
    return version + "\n"


def init_blueprint(
    cwd: Path,
    phase: str,
    *,
    force: bool = False,
) -> InitBlueprintResult:
    """Generate a leanblueprint-compatible directory from the phase plan index.

    Creates ``phases/{N}/blueprint/`` with:
      - ``content.tex`` — informal statements + ``\\lean{}`` refs
      - ``lakefile.lean`` — Lake project config
      - ``lean-toolchain`` — toolchain version pin
      - ``Proofs/`` — stub proof files
      - ``Blueprint.lean`` — module root

    Returns an ``InitBlueprintResult`` describing what was created.
    """
    from grd.core.lean.env import load_env  # noqa: PLC0415
    from grd.core.phases import find_phase, phase_plan_index  # noqa: PLC0415
    from grd.core.state import load_state_json  # noqa: PLC0415

    phase_info = find_phase(cwd, phase)
    if phase_info is None:
        return InitBlueprintResult(
            ok=False,
            phase=phase,
            blueprint_dir="",
            error=f"Phase {phase!r} not found. Run `grd phase list` to see available phases.",
        )

    blueprint_dir = cwd / phase_info.directory / "blueprint"
    if blueprint_dir.exists() and not force:
        return InitBlueprintResult(
            ok=False,
            phase=phase_info.phase_number,
            blueprint_dir=str(blueprint_dir.relative_to(cwd)),
            error=f"Blueprint directory already exists: {blueprint_dir.relative_to(cwd)}. Use --force to overwrite.",
        )

    # Build graph from plan index
    plan_idx = phase_plan_index(cwd, phase)
    nodes: list[BlueprintNode] = []
    edges: list[tuple[str, str]] = []

    for plan in plan_idx.plans:
        node = BlueprintNode(
            id=plan.id,
            label=plan.id,
            kind="theorem" if plan.wave == 1 else "lemma",
            wave=plan.wave,
            objective=plan.objective,
            depends_on=list(plan.depends_on),
        )
        nodes.append(node)
        for dep in plan.depends_on:
            edges.append((plan.id, dep))

    # If no plans exist, create a single placeholder node
    if not nodes:
        nodes.append(BlueprintNode(
            id="main",
            label="main",
            kind="theorem",
            objective="Main result (placeholder — add plans to this phase to populate the blueprint)",
        ))

    # Read Lean toolchain version
    lean_env = load_env(cwd)
    lean_version = lean_env.get("lean_version") if lean_env else None

    # Read project name from state
    state = load_state_json(cwd) or {}
    project_name = None
    if isinstance(state.get("project"), dict):
        project_name = state["project"].get("name")

    # Create directory structure
    blueprint_dir.mkdir(parents=True, exist_ok=True)
    proofs_dir = blueprint_dir / "Proofs"
    proofs_dir.mkdir(exist_ok=True)

    files_created: list[str] = []

    # content.tex
    content_tex = _generate_content_tex(
        nodes, edges, phase_info.phase_name, phase_info.phase_number, project_name
    )
    (blueprint_dir / "content.tex").write_text(content_tex, encoding="utf-8")
    files_created.append("content.tex")

    # lakefile.lean
    lakefile = _generate_lakefile(phase_info.phase_name, phase_info.phase_number)
    (blueprint_dir / "lakefile.lean").write_text(lakefile, encoding="utf-8")
    files_created.append("lakefile.lean")

    # lean-toolchain
    toolchain = _generate_lean_toolchain(lean_version)
    (blueprint_dir / "lean-toolchain").write_text(toolchain, encoding="utf-8")
    files_created.append("lean-toolchain")

    # Blueprint.lean (module root)
    root_lean = _generate_lean_root(nodes)
    (blueprint_dir / "Blueprint.lean").write_text(root_lean, encoding="utf-8")
    files_created.append("Blueprint.lean")

    # Proof stubs
    for node in nodes:
        stub = _generate_lean_stub(node.id)
        stub_path = proofs_dir / f"{_lean_module_name(node.id)}.lean"
        stub_path.write_text(stub, encoding="utf-8")
        files_created.append(f"Proofs/{_lean_module_name(node.id)}.lean")

    return InitBlueprintResult(
        ok=True,
        phase=phase_info.phase_number,
        blueprint_dir=str(blueprint_dir.relative_to(cwd)),
        files_created=files_created,
        node_count=len(nodes),
        edge_count=len(edges),
    )


