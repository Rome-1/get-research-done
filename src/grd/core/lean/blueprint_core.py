# Patterns for leanblueprint macros in content.tex
_ENVIRONMENT_RE = re.compile(
    r"\\begin\{(lemma|theorem|definition|proposition|corollary)\}"
    r"\s*\\label\{([^}]+)\}",
    re.DOTALL,
)
_LEAN_REF_RE = re.compile(r"\\lean\{([^}]+)\}")
_LEANOK_RE = re.compile(r"\\leanok\b")
_USES_RE = re.compile(r"\\uses\{([^}]+)\}")
_ENV_END_RE = re.compile(r"\\end\{(lemma|theorem|definition|proposition|corollary)\}")


def _parse_content_tex(content: str) -> tuple[list[BlueprintNode], list[tuple[str, str]]]:
    """Parse content.tex into blueprint nodes and edges."""
    nodes: list[BlueprintNode] = []
    edges: list[tuple[str, str]] = []

    # Split into environments by finding begin/end pairs
    pos = 0
    while pos < len(content):
        env_match = _ENVIRONMENT_RE.search(content, pos)
        if env_match is None:
            break

        kind = env_match.group(1)
        label = env_match.group(2)
        env_start = env_match.end()

        # Find the closing \end{kind}
        end_pattern = re.compile(rf"\\end\{{{re.escape(kind)}\}}")
        end_match = end_pattern.search(content, env_start)
        if end_match is None:
            pos = env_start
            continue

        env_body = content[env_start:end_match.start()]

        # Extract \lean{...}
        lean_match = _LEAN_REF_RE.search(env_body)
        lean_name = lean_match.group(1) if lean_match else None

        # Check for \leanok
        leanok = bool(_LEANOK_RE.search(env_body))

        # Extract \uses{...}
        uses_match = _USES_RE.search(env_body)
        deps: list[str] = []
        if uses_match:
            deps = [d.strip() for d in uses_match.group(1).split(",") if d.strip()]

        # Determine status
        if leanok:
            status = "proved"
        elif lean_name:
            status = "stated"
        else:
            status = "informal"

        node = BlueprintNode(
            id=label,
            label=label,
            kind=kind,
            lean_name=lean_name,
            leanok=leanok,
            status=status,
            depends_on=deps,
        )
        nodes.append(node)
        for dep in deps:
            edges.append((label, dep))

        pos = end_match.end()

    return nodes, edges


def _typecheck_lean_ref(
    lean_name: str,
    blueprint_dir: Path,
    project_root: Path,
) -> bool:
    r"""Check if a ``\lean{...}`` reference typechecks successfully.

    Looks for the corresponding .lean file under Proofs/ and runs
    ``grd lean typecheck-file`` against it.
    """
    # Convert lean module path to file path: Proofs.FooBar -> Proofs/FooBar.lean
    parts = lean_name.split(".")
    relative = Path(*parts).with_suffix(".lean")
    lean_file = blueprint_dir / relative

    if not lean_file.is_file():
        return False

    try:
        from grd.core.lean.client import check_file  # noqa: PLC0415

        result = check_file(
            path=lean_file,
            project_root=project_root,
            timeout_s=60.0,
            use_daemon=True,
            auto_spawn=True,
        )
        return result.ok
    except Exception:
        return False


def _render_ascii_graph(graph: BlueprintGraph) -> str:
    """Render a simple ASCII dependency graph."""
    if not graph.nodes:
        return "(empty blueprint)"

    lines: list[str] = []
    status_symbols = {
        "proved": "[OK]",
        "stated": "[--]",
        "informal": "[  ]",
        "failed": "[!!]",
    }
    color_hints = {
        "proved": "green",
        "stated": "yellow",
        "informal": "grey",
        "failed": "red",
    }

    # Group by wave
    waves: dict[int, list[BlueprintNode]] = {}
    for node in graph.nodes:
        waves.setdefault(node.wave, []).append(node)

    for wave_num in sorted(waves):
        lines.append(f"Wave {wave_num}:")
        for node in waves[wave_num]:
            sym = status_symbols.get(node.status, "[??]")
            deps_str = ""
            if node.depends_on:
                deps_str = f" ← {', '.join(node.depends_on)}"
            lean_str = ""
            if node.lean_name:
                lean_str = f" ({node.lean_name})"
            lines.append(f"  {sym} {node.id}{lean_str}{deps_str}")
        lines.append("")

    # Summary line
    total = len(graph.nodes)
    proved = sum(1 for n in graph.nodes if n.status == "proved")
    stated = sum(1 for n in graph.nodes if n.status == "stated")
    informal = sum(1 for n in graph.nodes if n.status == "informal")
    failed = sum(1 for n in graph.nodes if n.status == "failed")
    lines.append(f"Legend: [OK]=proved  [--]=stated  [  ]=informal  [!!]=failed")
    lines.append(f"Total: {total}  proved: {proved}  stated: {stated}  informal: {informal}  failed: {failed}")

    return "\n".join(lines)


def blueprint_status(
    cwd: Path,
    phase: str,
    *,
    typecheck: bool = True,
) -> BlueprintStatusResult:
    """Walk a phase's blueprint and report formalization status.

    For each ``\\lean{...}`` reference in ``content.tex``, optionally runs
    ``grd lean typecheck-file`` to verify the Lean source compiles, and
    auto-marks nodes whose proofs pass as ``\\leanok``.

    Returns a ``BlueprintStatusResult`` with the full graph, summary counts,
    and an ASCII rendering of the dependency graph.
    """
    from grd.core.phases import find_phase  # noqa: PLC0415

    phase_info = find_phase(cwd, phase)
    if phase_info is None:
        return BlueprintStatusResult(
            ok=False,
            phase=phase,
            graph=BlueprintGraph(phase=phase),
            error=f"Phase {phase!r} not found.",
        )

    blueprint_dir = cwd / phase_info.directory / "blueprint"
    content_tex = blueprint_dir / "content.tex"

    if not content_tex.is_file():
        return BlueprintStatusResult(
            ok=False,
            phase=phase_info.phase_number,
            graph=BlueprintGraph(phase=phase_info.phase_number),
            error=f"No blueprint found. Run `grd lean init-blueprint --phase {phase}` first.",
        )

    content = content_tex.read_text(encoding="utf-8")
    nodes, edges = _parse_content_tex(content)

    # Typecheck each node with a \lean{...} reference
    leanok_updates: list[str] = []
    for node in nodes:
        if node.lean_name and not node.leanok and typecheck:
            ok = _typecheck_lean_ref(node.lean_name, blueprint_dir, cwd)
            if ok:
                node.leanok = True
                node.status = "proved"
                leanok_updates.append(node.id)
            else:
                if node.status == "stated":
                    node.status = "failed"

    # Auto-write \leanok into content.tex for newly passing proofs
    if leanok_updates:
        updated_content = _auto_mark_leanok(content, leanok_updates)
        content_tex.write_text(updated_content, encoding="utf-8")

    graph = BlueprintGraph(phase=phase_info.phase_number, nodes=nodes, edges=edges)

    summary = {
        "total": len(nodes),
        "proved": sum(1 for n in nodes if n.status == "proved"),
        "stated": sum(1 for n in nodes if n.status == "stated"),
        "informal": sum(1 for n in nodes if n.status == "informal"),
        "failed": sum(1 for n in nodes if n.status == "failed"),
        "leanok_updated": len(leanok_updates),
    }

    ascii_graph = _render_ascii_graph(graph)

    return BlueprintStatusResult(
        ok=True,
        phase=phase_info.phase_number,
        graph=graph,
        summary=summary,
        ascii_graph=ascii_graph,
    )


def _auto_mark_leanok(content: str, node_ids: list[str]) -> str:
    """Insert ``\\leanok`` after ``\\lean{...}`` for nodes that now typecheck.

    Only modifies environments whose ``\\label{...}`` matches a node_id in
    the update list. Idempotent — won't double-add if ``\\leanok`` is already
    present.
    """
    ids_remaining = set(node_ids)

    def _replacer(match: re.Match) -> str:
        label_match = re.search(r"\\label\{([^}]+)\}", match.group(0))
        if label_match is None or label_match.group(1) not in ids_remaining:
            return match.group(0)

        body = match.group(0)
        if _LEANOK_RE.search(body):
            return body  # already marked

        # Insert \leanok on the line after \lean{...}
        lean_match = _LEAN_REF_RE.search(body)
        if lean_match:
            insert_pos = lean_match.end()
            body = body[:insert_pos] + "\n  \\leanok" + body[insert_pos:]
        return body

    # Match full environments
    full_env_re = re.compile(
        r"\\begin\{(lemma|theorem|definition|proposition|corollary)\}"
        r".*?"
        r"\\end\{\1\}",
        re.DOTALL,
    )
    return full_env_re.sub(_replacer, content)
