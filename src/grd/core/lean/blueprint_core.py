"""Blueprint scaffolding and status — ``grd lean init-blueprint`` / ``blueprint-status``.

Generates a leanblueprint-compatible LaTeX skeleton from the GRD phase
task graph and checks formalization status of each node.

Mapping:
  GRD plan entry  →  blueprint ``\\begin{lemma}`` / ``\\begin{theorem}`` node
  plan ``depends_on``  →  ``\\uses{...}`` edge
  Lean typecheck pass  →  ``\\leanok`` marker
  ``\\lean{Module.name}``  →  cross-reference to Lean source file

The dependency graph can be rendered as ASCII (default), SVG (with
graphviz), or JSON for agent consumption.
"""

from __future__ import annotations

import re
import textwrap
from dataclasses import dataclass, field
from pathlib import Path

from pydantic import BaseModel, Field

__all__ = [
    "BlueprintNode",
    "BlueprintGraph",
    "InitBlueprintResult",
    "BlueprintStatusResult",
    "init_blueprint",
    "blueprint_status",
]


# ---------------------------------------------------------------------------
# Data models
# ---------------------------------------------------------------------------


class BlueprintNode(BaseModel):
    """One node in the blueprint dependency graph."""

    id: str
    label: str
    kind: str = "lemma"  # lemma | theorem | definition
    lean_name: str | None = None  # e.g. "Proofs.PlanA" — from \lean{...}
    leanok: bool = False
    status: str = "informal"  # informal | stated | proved | failed
    depends_on: list[str] = Field(default_factory=list)
    wave: int = 1
    objective: str | None = None


class BlueprintGraph(BaseModel):
    """Full dependency graph for one phase's blueprint."""

    phase: str
    nodes: list[BlueprintNode] = Field(default_factory=list)
    edges: list[tuple[str, str]] = Field(default_factory=list)  # (from, to)


class InitBlueprintResult(BaseModel):
    """Result of ``grd lean init-blueprint``."""

    ok: bool
    phase: str
    blueprint_dir: str
    files_created: list[str] = Field(default_factory=list)
    node_count: int = 0
    edge_count: int = 0
    error: str | None = None


class BlueprintStatusResult(BaseModel):
    """Result of ``grd lean blueprint-status``."""

    ok: bool
    phase: str
    graph: BlueprintGraph
    summary: dict[str, int] = Field(default_factory=dict)
    ascii_graph: str | None = None
    error: str | None = None


